import socket
import time

def netcat(host, port, content):
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.connect((host, int(port)))
    s.sendall(content.encode())
    time.sleep(1)
    s.shutdown(socket.SHUT_WR)
    while True:
        data = s.recv(4096)
        if not data:
            break
        print(repr(data))
    s.close()
for i in range(1,11):
    netcat('localhost',5000, 'MESSAGE FROM CLIENT - {}'.format(i))